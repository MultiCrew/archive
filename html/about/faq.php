	<h1 class="mb-3">Mission Statement</h1>

	<h4>What is MultiCrew's goal?</h4>
	<p>Our main goal is to increase participation and skill levels in shared cockpit flying in consumer and commercial flight simulators. Basically, that means we want you to fly more in shared cockpit setups!</p>

	<h4>How will MultiCrew achieve its goal?</h4>
	<p>Simply, by providing means to fly and train flight simmers in good-quality (usually payware), accurate and in-depth addons for the ESP flight simulators (FSX and P3D) which support high-fidelity shared cockpit flights.

