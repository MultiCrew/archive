	<h1 class="mb-3">About MultiCrew</h1>

	<h4>What is MultiCrew?</h4>
	<p>MultiCrew is a community-driven flight simulation organisation that provides training and support for shared cockpit aircraft addons. We are a community-driven, non-profit organisation, which specialises in shared cockpit flying, training and support.</p>

	<h4>Who runs it?</h4>
	<p>The main Administrators who drive the projects and development along are two students from Scotland. You can see our full staff lineup on our <a href="staff.php">Staff page</a>. All our staff are volunteers!</p>

	<h4>When did it start?</h4>
	<p>MultiCrew was started in early 2017 and has been expanding ever since.</p>

	<h4>What does MultiCrew actually do?</h4>
	<p>We provide services to the flight simulation community. These include both informal training and pilot ratings for traffic networks, unofficial support, and means to organise shared cockpit flights.</p>

	<h4>What's in development?</h4>
	<p>We're developing many more web-based systems, courses and packages to help encourage participation in shared cockpit, and to help the community as a whole. You can read more about the recent developments on our <a href="../blog">Dev Blog</a>!</p>

